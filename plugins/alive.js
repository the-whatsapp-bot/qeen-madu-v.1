const resizeImage = async (buffer, width, height) => {
    try {
        const image = await jimp.read(buffer);
        return await image.resize(width, height).getBufferAsync(jimp.MIME_JPEG);
    } catch (error) {
        console.error('Error resizing image:', error);
        throw error;
    }
};




